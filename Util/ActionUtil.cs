        /// <summary>
        /// Query action message by character
        /// </summary>
        /// <param name="action">Action name</param>
        /// <param name="chara">Character</param>
        /// <returns>Action message</returns>
        public static string GetCharaActionMsg(string action, CharacterUtil.EChara chara)
        {
            string msg;

            if (string.IsNullOrEmpty(action)
                || string.IsNullOrWhiteSpace(action))
            {
                return "Empty Action";
            }

            string resId = CharacterUtil.GetCharaResID(chara);
            string msgId = "Cx" + resId + "_" + action;

            if (!ACTIONS.TryGetValue(resId + action, out msg))
            {
                // Return message ID if action is invalid
                return msgId;
            }

            return msg;
        }

        /// <summary>
        /// Query common action message
        /// </summary>
        /// <param name="action">Action name</param>
        /// <returns>Action message</returns>
        public static string GetCmnActionMsg(string action)
        {
            return GetCharaActionMsg(action, CharacterUtil.EChara.COMMON);
        }

